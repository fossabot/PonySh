Shetland ponies are around 107 cm tall after four years of age.
Small ponies have existed in the Shetland Isles for over 2000 years.
Numerous children used Shetland ponies for riding and driving.
For their size, they are the strongest of all the horse breeds.
When a law in 1847 banned children from entering the coal pits, the Shetland pony colts became in high demand.
The Shetland Pony is thought to have descended from a cob-type pony from Southern Europe, and a pony brought to the Shetland Islands by the Celtic people.
Several Shetland Ponies were owned by Queen Victoria to drive some of her carriages.
The Shetland Pony Stud-Book Society was formed in 1890.
Their coat changes according to the season, which helps protect them against the often harsh conditions of the islands
The Independent wrote an article in 2016 stating that,"There are 50 ponies to every one human being on Foula Island [part of the Shetland islands]".
