"Dartmoor Ponies are the native pony breed of the county of Devon in the South West of England" The Dartmoor Pony Society
Its scientific name is 'Equus Ferus Caballus'
Breeds are no taller than 127 cm.
They can come in the colours chestnut, bay, grey, black, roan, or brown.
The earliest mention of the breed happened in 1012 when the Bishop of Crediton noted a Dartmoor Pony in his will.
The devastation caused by World War II reduced the number of Dartmoor ponies, leading nearly to the breed dying out.
In 1988, the Dartmoor Pony Moorland Scheme was established by the Duchy of Cornwall and the Dartmoor Pony Society.
According to the Dartmoor National Park Authority, there are fewer than 3,000 ponies found on the moor today, from approximately 30,000 in 1950.
Though Exmoor ponies live reasonably close geographically and their spots are somewhat similar, Dartmoor and Exmoor ponies are now thought not to be related.
