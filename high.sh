The Highland Pony is a robust, well-balanced pony breed with all its features being in proportion to its body
Their height ranges from 1.32m-1.44m
The most colours include chestnut, black, brown, brown with black spots (otherwise known as bay) and grey.
Many also have markings of some sort, including dorsal stripes and zebra markings on the legs.
Generally speaking, they have a short fine-featured head with a broad forehead, muscled neck and short legs with light feathering. Many Highland Ponies have zebra markings, and the animals themselves are native to the Scottish Highlands.
Their average lifespan is 25-30 years, and they were never purposely cross-bred to other animals.
The Highland Pony Society was formed in 1923, and the pony is currently listed as 'at risk' by the Rare Breeds Survival Trust.
Highland Ponies are often used for forestry work; they have also been used for trekking, driving, removing deer carcases, and for children.
They are the most powerful of all the native pony breeds in Great Britain, and today, there are an estimated 5,500 Highlands in the world, with most in Europe.
